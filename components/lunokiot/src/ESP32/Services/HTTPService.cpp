/* Function for starting the webserver */
httpd_handle_t start_webserver(void)
{
    /* Generate default configuration */
    httpd_config_t config = HTTPD_DEFAULT_CONFIG();

    /* Empty handle to esp_http_server */
    httpd_handle_t server = NULL;

    /* Start the httpd server */
    if (httpd_start(&server, &config) == ESP_OK) {
        /* Register URI handlers */
        httpd_register_uri_handler(server, &uri_get);
        //httpd_register_uri_handler(server, &uri_post);
    }
    /* If server failed to start, handle will be NULL */
    return server;
}

/* Function for stopping the webserver */
void stop_webserver(httpd_handle_t server)
{
    if (server) {
        /* Stop the httpd server */
        httpd_stop(server);
    }
}

using namespace LunokIoT;


HTTPService::HTTPService() : ServiceTemplate((const char *)"(-) HTTP Service", 7000, 3000 ) {
    //Reconfigure task to = (unsigned long)CONFIG_LWIP_SNTP_UPDATE_DELAY
    debug_printf("Setup");
}


bool HTTPService::Loop() {
    // silent until online
    bool connected = WiFiDriver::instance->IsOnline();
    if ( connected ) {
        if ( false == started ) {
            debug_printf("HTTP server starting....");
            handler = start_webserver();
            started = true;
            return true;
        }
    } else {
        if ( started ) {
            debug_printf("HTTP server STOP due no connectivity");
            stop_webserver(handler);
            started = false;
        }
    }
    return true;
}
